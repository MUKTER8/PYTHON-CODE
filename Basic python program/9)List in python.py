#learn List in python
#List is a collection which is ordered and changeable. Allows duplicate members. 
#In Python lists are written with square brackets.
#Syntax: list = [item1, item2, item3, ...]
#Example: fruits = ["apple", "banana", "cherry"]

student=["MUKTER",80,"Dhaka","Bangladesh"]
print(student[3])
print((student[1:3])) #print index 1 to 3
print(student[-1]) #print last index
print(student[-3:-1]) #print last index to 1
print(student[-3:]) #print last index to all
print(student[0:len(student)]) #print index 0 to all